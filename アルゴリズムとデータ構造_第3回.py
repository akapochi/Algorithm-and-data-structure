"""## 最大公約数

**問：2つの自然数a, bの最大公約数を求めよ．**

### 解法１：
1.   変数xにbを代入
2.   aとbをxで割り，両方割り切れたらxを出力
3.   変数xの値を一つ減らす．2. に戻る．
"""

def gcd_sol1(a, b):
  x = b
  while True: # while Trueは無限ループ
    if a % x == 0 and b % x == 0: # a % x は a を x で割ったときの余り
      return x
    x -= 1

