"""### 解法２：ユークリッドの互除法(Euclidean algorithm)
1.   aをbで割った余りをrとする．
2.   rが0ならば，bを出力して終了する．
3.   aにbを，bにrを代入して1．へ戻る．


"""

def gcd_euclidean_algorithm(a, b):
  while True: # 無限ループ
    r = a % b
    if r == 0:
      return b # r=0のときにbを出力して終了する．
    a, b = b, r

print("a,b=",a, b)
